<h3 id="object-isfrozen-object-isseal" tabindex="-1"><a class="header-anchor" href="#object-isfrozen-object-isseal"><span>Object.isFrozen() &amp;&amp; Object.isSeal()</span></a></h3>
<ul>
<li>描述</li>
</ul>
<p>Object.isFrozen()方法判断一个对象是否被冻结。 一个对象是冻结的是指它不可扩展，所有属性都是不可配置的，且所有数据属性（即没有 getter 或 setter 组件的访问器的属性）都是不可写的。</p>
<p>Object.isSealed() 方法判断一个对象是否被密封。如果这个对象是密封的，则返回 true，否则返回 false。密封对象是指那些不可 扩展 的，且所有自身属性都不可配置且因此不可删除（但不一定是不可写）的对象。</p>
<p>一个不可扩展的空对象是一个冻结对象</p>
<p>一个冻结对象是一个密封对象</p>
<p>把一个对象的所有属性都改为不可配置就会变成一个冻结对象</p>
<p>一个不可扩展的空对象,是密封对象</p>
