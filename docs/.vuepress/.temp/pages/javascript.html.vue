<p><em>configurable</em> 表示对象的属性是否可以被删除，以及除 value 和 writable 特性外的其他特性是否可以被修改。默认为 false。
如果 configurable 属性设置为 false，则该属性被认为是“不可配置的”，并且没有属性可以被改变 <em><span style="color:#F56C6C">（除了单向改变 writable 为 false）</span></em> 。
当属性不可配置时，不能在数据和访问器属性类型之间切换。但如果 writeable 为 true 时,该属性是仍然可以改变值<br/></p>
<p><em>enumerable</em> 配置对象的属性是否会被枚举,当且仅当该属性的 enumerable 键值为 true 时，该属性才会出现在对象的枚举属性中。默认为 false。但如果使用直接赋值的方式创建对象的属性，则 enumerable 为 true;</p>
<ul>
<li>数据描述符独有的配置项</li>
</ul>
<p><em>value</em> 该属性对应的值。可以是任何有效的 JavaScript 值（数值，对象，函数等）。默认为 undefined。<br/></p>
<p><em>writable</em> 当且仅当该属性的 writable 键值为 true 时，属性的值，也就是上面的 value，才能被赋值运算符改变。默认为 false。</p>
<ul>
<li>存取描述符独有的配置项</li>
</ul>
<p><em>get</em> 属性的 getter 函数，如果没有 getter，则为 undefined。当访问该属性时，会调用此函数。执行时不传入任何参数，但是会传入 this 对象（由于继承关系，这里的 this 并不一定是定义该属性的对象）。该函数的返回值会被用作属性的值。默认为 undefined。<br/></p>
<p><em>set</em> 属性的 setter 函数，如果没有 setter，则为 undefined。当属性值被修改时，会调用此函数。该方法接受一个参数（也就是被赋予的新值），会传入赋值时的 this 对象。默认为 undefined。</p>
